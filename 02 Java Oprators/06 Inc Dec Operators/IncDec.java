// Increment / Decrement Operators Example

public class IncDec {

   public static void main(String[] args) {

      int a = 12;

      // Increment 'a' by 1 using the increment operator (equivalent to a = a + 1)
      a++;

      // Decrement 'a' by 1 using the decrement operator (equivalent to a = a - 1)
      a--;

      System.out.println("Final value of 'a': " + a);
   }
}